# 세트의 합집합
# 연산자 : |
# 메소드 : set.union(...)

s1 = set("13579")
s2 = set("12468")

# 합집합 : |
# 중복되는 요소는 하나만 선택
s3 = s1 | s2

print(s1) # {'5', '9', '3', '7', '1'}
print(s2) # {'6', '2', '4', '8', '1'}
print(s3) # {'5', '6', '2', '9', '4', '3', '7', '8', '1'}

#%%

s4 = s1.union(s2)
print(s4) # {'5', '6', '2', '9', '4', '3', '7', '8', '1'}